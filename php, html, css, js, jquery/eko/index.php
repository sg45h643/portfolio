            <h2>Żywność ekologiczna</h2>
            <p>Żywność ekologiczna – określenie żywności produkowanej metodami rolnictwa ekologicznego z dbałością o <strong>wyeliminowanie</strong> używania nawozów sztucznych i pestycydów.</p>
            <h3>Czym jest żywność ekologiczna</h3>
            <p>Ekologiczne metody produkcji żywności mają zapewniać <strong>ochronę zdrowia</strong> społeczeństwa i środowiska i stanowić system zrównoważony pod względem:</p>
                <ul>
                    <li>ekologicznym – nie obciąża środowiska w stopniu większym niż naturalne ekosystemy;</li>
                    <li>ekonomicznym – jest w dużym stopniu niezależny od nakładów zewnętrznych;</li>
                    <li>społecznym – umożliwia zachowanie oraz rozwój wsi i rolnictwa.</li>
                </ul>
            <p>Producenci i przetwórcy żywności <strong>ekologicznej</strong> mają obowiązek oznaczenia swoich produktów certyfikatami. W Polsce na koniec 2008 stosowane są certyfikaty 11 firm, między innymi: Agrobiotest, Bioeksperta, Biocert, Ekogwarancja PTRE, Cobico, PCBC i PNG.</p>
            <p>W Unii Europejskiej produkcja takiej żywności jest regulowana rozporządzeniem, czyli aktem prawnym najwyższej rangi (Rozporządzenie Rady EWG nr 2092/91, z dnia 1.01.93 r., w sprawie rolnictwa ekologicznego oraz znakowania jego produktów i <strong>środków spożywczych</strong>, obowiązujące w państwach członkowskich UE). Od maja 2004 rozporządzenie to obowiązuje również w Polsce.</p>
            <p>Potocznie określenie "żywność ekologiczna" często bywa używane zamiennie z określeniem "zdrowa żywność". Jednakże podczas gdy pierwsze z pojęć określa <strong>żywność certyfikowaną</strong>, drugie jest w zasadzie tylko nazwą stworzoną dla celów marketingowych i nie narzucającą żadnych określonych standardów.</p>
            <h3>Polski rynek żywności ekologicznej</h3>
            <p>Polski rynek żywności ekologicznej jest obecnie szacowany na ok. 100 mln. €. Żywność certyfikowaną kupuje już ok. 7% polskich konsumentów – głównie w specjalistycznych sklepach (około 200), na półkach zlokalizowanych w ekologicznych wyspach w supermarketach oraz bezpośrednio u rolników. Powierzchnia polskich <strong>upraw ekologicznych</strong> rośnie dynamicznie. Wg danych opublikowanych w raporcie IJHARS w 2008 roku funkcjonowało w Polsce 14.896 gospodarstw ekologicznych obejmujących łącznie obszar 314,9 tys. hektarów (tj. 1% powierzchni całego kraju oraz 2% powierzchni przeznaczonej na uprawy rolne).</p>
